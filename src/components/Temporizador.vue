<template>
    <div class="is-flex is-align-items-center is-justify-content-space-between">

        <Cronometro :tempoEmSegundo="tempoEmSegundo" />
        <button class="button" @click="iniciar" :disable="cronometroRodando">
            <span class="icon">
                <i class="fas fa-play"></i>
            </span>
            <span>play</span>
        </button>
        <button class="button" @click="finalizar" :disable="cronometroRodando">
            <span class="icon">
                <i class="fas fa-stop"></i>
            </span>
            <span>stop</span>
        </button>
    </div>
</template>

<script lang="ts">

import { defineComponent } from 'vue';
import Cronometro from './Cronometro.vue'

export default defineComponent({
    name: "Temporizador",
    emits: ['aoTemporizadorFinalizado'],
    components: {
        Cronometro

    },
    data() {
        return {
            tempoEmSegundo: 0,
            cronometro: 0,
            cronometroRodando: false
        }
    },

    methods: {
        iniciar() {
            // comeÃ§ar a contagem
            //1s = a 1000ms
            this.cronometroRodando = true
            this.cronometro = setInterval(() => {
                this.tempoEmSegundo += 1
            }, 1000)


        },
        finalizar() {
            this.cronometroRodando = false
            clearInterval(this.cronometro)
            this.$emit('aoTemporizadorFinalizado', this.tempoEmSegundo)
            this.tempoEmSegundo = 0

        }
    }
});
</script>